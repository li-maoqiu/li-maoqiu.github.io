const tooltip=document.getElementById('tooltip');
const hoverItems=document.querySelectorAll('.dttc');

hoverItems.forEach(item=>{
    item.addEventListener('mouseenter',(e)=>{
        let text=item.dataset.tooltip.split("／");
        tooltip.innerHTML=`<small>${text[0]}</small>`+((text[1])?"<br>"+text[1]:"");
        document.getElementById("dy_sty").innerHTML="."+text[0]+"{box-shadow:inset 100px 100px #74B8E580 !important;}";
        tooltip.style.display='block';
    });

    item.addEventListener('mousemove',(e)=>{
        const x=e.clientX+10;
        const y=e.clientY+10;
        tooltip.style.left=x+'px';
        tooltip.style.top=y+'px';
    });

    item.addEventListener('mouseleave',()=>{
        tooltip.style.display='none';
        document.getElementById("dy_sty").innerHTML="";
    });
});